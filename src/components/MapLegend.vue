<template>

  <div class="legend fr-col-lg-2">
    <div class="legend_container">
      <p class="l_box_title fr-text fr-mb-1w">LÃ©gende</p>
      <div class="scale fr-grid-row" style="grid-column-gap: 0.5rem; justify-content:flex-start;" v-if="map">
        <div class="scale_container"> </div>
        <div class="fr-col-2" style="display: flex; flex-direction: column;">
          <span class="min fr-text--sm fr-text--bold fr-mb-1">{{convertNumberToHuman(min)}}</span>
          <span class="max fr-text--sm fr-text--bold fr-mb-1">{{convertNumberToHuman(max)}}</span>
        </div>
      </div>
    </div>
  </div>

</template>

<script>

export default {
  name: 'LeftCol',

  data(){
    return {
      isDown:[false,false],
      isGreen:[false,false],
      isRed:[false,false],
      isBlue:[false,false]
    }
  },
  props: {
    date: String,
    localisation: String,
    values: Array,
    names: Array,
    evolcodes: Array,
    evolvalues : Array,
    min: Number,
    max: Number,
    map: Boolean
  },
  computed: {

  },
  methods: {

    convertNumberToHuman(float){
      return parseFloat(float).toLocaleString()
    },

    testEvolStyle(){
      var self = this
      this.names.forEach(function(n,i){
        if(self.evolcodes[i]=="green"){
          self.isGreen[i] = true
          self.isRed[i] = false
          self.isBlue[i] = false
          self.evolvalues[i] > 0 ? self.isDown[i] = false : self.isDown[i] = true
        }else if(self.evolcodes[i]=="red"){
          self.isGreen[i] = false
          self.isRed[i] = true
          self.isBlue[i] = false
          self.evolvalues[i] > 0 ? self.isDown[i] = false : self.isDown[i] = true
        }else{
          self.isGreen[i] = false
          self.isRed[i] = false
          self.isBlue[i] = true
          self.isDown[i] = false
        }
      })
    }

  },

  watch:{
    evolcodes:function(){
      this.testEvolStyle()
    },
    evolvalues:function(){
      this.testEvolStyle()
    }
  },

  created(){
    this.testEvolStyle()
  },

}
</script>

